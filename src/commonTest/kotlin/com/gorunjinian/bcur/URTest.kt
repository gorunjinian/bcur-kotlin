    @Test
    fun testMultipartUR() {
        val ur = TestUtils.makeMessageUR(32767, "Wolf")
        val urEncoder = UREncoder(ur, 1000, 10, 100)
        val urDecoder = URDecoder()

        do {
            val part = urEncoder.nextPart()
            urDecoder.receivePart(part)
        } while (urDecoder.result == null)

        assertEquals(ResultType.SUCCESS, urDecoder.result!!.type)
        val decodedUR = urDecoder.result!!.ur!!
        assertEquals(ur, decodedUR)
    }

    @Test
    fun testDuplicateDecode() {
        val parts = listOf(
            "ur:bytes/1-9/lpadascfadaxcywenbpljkhdcahkadaemejtswhhylkepmykhhtsytsnoyoyaxaedsuttydmmhhpktpmsrjtdkgslpgh",
            "ur:bytes/2-9/lpaoascfadaxcywenbpljkhdcagwdpfnsboxgwlbaawzuefywkdplrsrjynbvygabwjldapfcsgmghhkhstlrdcxaefz",
            "ur:bytes/3-9/lpaxascfadaxcywenbpljkhdcahelbknlkuejnbadmssfhfrdpsbiegecpasvssovlgeykssjykklronvsjksopdzmol"
        )

        val urDecoder = URDecoder()
        assertEquals(0, urDecoder.processedPartsCount)
        urDecoder.receivePart(parts[0])
        assertEquals(1, urDecoder.processedPartsCount)
        urDecoder.receivePart(parts[0])
        assertEquals(1, urDecoder.processedPartsCount)
        urDecoder.receivePart(parts[1])
        assertEquals(2, urDecoder.processedPartsCount)
        urDecoder.receivePart(parts[0])
        assertEquals(2, urDecoder.processedPartsCount)
        urDecoder.receivePart(parts[2])
        assertEquals(3, urDecoder.processedPartsCount)
    }
